/* ***********************************************************
 * Namespace: xSprite
 * Enum:  RotationAnchorPoint
 * 
 * Enumeration to define the anchor point a sprite rotates
 * around.
 * *********************************************************** */

namespace xSprite
{
    /// <summary>
    /// The anchor point that the sprite will pivot around when rotated.
    /// </summary>
    public enum RotationAnchorPoint
    {
        TopLeft,
        TopCenter,
        TopRight,
        CenterLeft,
        Center,
        CenterRight,
        BottomLeft,
        BottomCenter,
        BottomRight
    } // <-- end RotationAnchorPoint enum
}
